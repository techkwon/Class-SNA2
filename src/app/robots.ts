import type { MetadataRoute } from "next";

export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: "*",
        allow: "/",
      },
    ],
    sitemap: "https://class-sna-2.vercel.app/sitemap.xml",
    host: "https://class-sna-2.vercel.app",
  };
}
